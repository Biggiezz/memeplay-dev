const MAPS = [MAP1, MAP2, MAP3];

const GATE_POSITIONS = [
  [[16, 17], [16, 18], [17, 17], [17, 18]], // Map 1
  [[17, 17], [17, 18], [18, 17], [18, 18]], // Map 2 - gate ở trung tâm
  [[17, 17], [17, 18], [18, 17], [18, 18]] // Map 3 - gate ở trung tâm
];

function getGatePositions(mapIndex) {
  return GATE_POSITIONS[mapIndex] || [[16, 17], [16, 18]];
}

function getPathTiles(map) {
  const pathTiles = [];
  for (let row = 0; row < map.length; row++) {
    for (let col = 0; col < map[row].length; col++) {
      if (map[row][col] === 0) {
        pathTiles.push([row, col]);
      }
    }
  }
  return pathTiles;
}

